package day04_variables_intro;

public class FirstVariable {
    public static void main (String [] args){
        // declare variable n that it can store int (whole number)
        int n = 10;         //DATA TYPE + VARIABLE = VALUE
        System.out.println (n);  // WHEN PRINTING VARIABLE, DOUBLE QUOTE "" IS NOT NEEDED!!!
        System.out.println ("n"); // prints out n because "" are used!!!

        int a = 33;
        System.out.println (a);

        int apples = 50;
        System.out.println (apples);

        int apples2;
        apples2 = 50;
        System.out.println (apples2);

        int bananas;
        bananas = 100;
        System.out.println (bananas);
    }
}
