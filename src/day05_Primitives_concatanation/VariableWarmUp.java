package day05_Primitives_concatanation;

public class VariableWarmUp {
    public static void main (String [] args){
        int count = 10;
        System.out.println(count);

        System.out.println("count"); // will print word because of ""

        count = 25;    // changed count value, need to print again to get 25 printed (top to bottom!)
        System.out.println(count);

    }
}
