package day05_Primitives_concatanation;

public class WatchInfo {
    public static void main(String[] args) {
        String brand = "Chopard";
        String color = "Golden White";
        int price = 10000;
        boolean waterResistant = true;
        boolean isSmart = false;
        char gender = 70;               // OR = 'F';

        System.out.println("Brand is:\t\t\t" + brand);
        System.out.println("Color is:\t\t\t" + color);
        System.out.println("Price is:\t\t\t" + price);
        System.out.println("Water Resistant:\t" + waterResistant);
        System.out.println("Smart Watch:\t\t" + isSmart);
        System.out.println("Gender:\t\t\t\t" + gender);
    }
}
