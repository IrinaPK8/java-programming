package day09_scanner_practice;

public class RemainderOperator {

    public static void main (String [] args) {

        System.out.println(10 / 3); // 3
        System.out.println(10 % 3); // remainder 1

        System.out.println (10 / 5); // 2
        System.out.println (10 % 5); // 0 remainder

        System.out.println (20 / 7); // 2
        System.out.println (20 % 7); //6 remainder

        System.out.println(115 % 100); // to calculate change fot $1 from 115 cents
        System.out.println(100 % 60); // 100 minutes - how many hours is in there - 1 hr 40 min

    }
}
